	<div class="top-help-title-1">1. ROOtKEYとは？</div>
	<p>ROOtKEYは出発地と目的地の間にあるものを検索することができるツールです。</p>
	<p>例えば「大阪駅」から「大阪城」の間にある「文化財」を以下のように検索することができます。</p>
	<ul class="clearing-thumbs" data-clearing>
		<li><a href="<?php echo Uri::create('assets/img/help1.png'); ?>">
			<?php echo Asset::img('help1.png', array('alt' => 'ROOtKEYヘルプ画像1')); ?>
		</a></li>
	</ul>
	<p class="text-right"><small>（画像をクリックすると拡大します）</small></p>
	<p>旅行のプランを作る、今いる場所から行き先までの探しものの用途にお使い下さい。</p>

	<div class="top-help-title-1">2. 使い方</div>
	<p>左のフォームに以下の必要な項目を入力して検索するだけです。</p>
	<p>
		<ul class="square">
			<li>出発地：出発地を入力します。
			<li>目的地：目的地を入力します。
			<li>検索ワード：探したいものを入力します。
			<li>移動手段：「自動車」「徒歩」の2種類で、通る道が変わります。
			<li>検索半径：検索を行う地図上の範囲が変えられます。理想の道からどの距離まで外れることができるのかを基準に選びます。
		</ul>
	</p>
	<ul class="clearing-thumbs" data-clearing>
