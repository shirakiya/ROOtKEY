		</a></li>
	</ul>
	<p class="text-right"><small>（画像をクリックすると拡大します）</small></p>
	<div class="top-help-title-1">3. 共有する</div>
	<p>ROOtKEYは検索結果を共有することもできます。</p>
	<ol>
		<li>画面右上の「ログイン」からアカウントを登録します。（Facebook、Twitterログインが使えます）</li>
		<li>検索を行い、検索結果を保存します。</li>
		<li>「マイページ」から共有したい画像を探します。</li>
		<li>お好きなSNSのシェアボタンをクリックし、投稿してください</li>
	</ol>
